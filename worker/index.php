<p>
A <em>Worker</em> is a script you run on a spare workstation that
is responsible for compiling and executing programs submitted in
the programming contest. It is a Perl script, so make sure the
machine you run it on has Perl installed on it.
</p>
<p>
Open a command prompt.
Create a new empty directory to be the working
directory.
Download the <a href="scripts/worker.pl">worker script</a> (Perl).
Make sure <tt>javac</tt>, <tt>java</tt>, <tt>gcc</tt>,
and <tt>g++</tt> are in your PATH.
Run the worker script (e.g. <tt>perl worker.pl</tt>).
Press Ctrl+C when you want it to stop.
</p>
<p>
You can run multiple instances of the Worker script,
one-per-CPU, if you like.
</p>

<h2>Running a worker on Windows</h2>
<p>
Install recent version of Perl.
</p>

